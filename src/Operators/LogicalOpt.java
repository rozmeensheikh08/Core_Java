package Operators;

public class LogicalOpt {
    public static void main(String[] args) {
        int a = 10;
        int b = 5;

        System.out.println(" a is greater then b : " );
        System.out.println(a>b);

        System.out.println();

        System.out.println(" a is less then b : " );
        System.out.println(a<b);
        System.out.println(" a is not greater then b : " );
        System.out.println(!(a>b));




    }
}
